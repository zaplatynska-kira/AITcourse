public class HWMethodVoid00 {
    public static void main(String[] args) {
        int a;
        a = 2;
        int b;
        b = 5;
        int result;
        result = 1;
        for (int i = 1; i <= b; i++) {
            result = result * a;
        }
        System.out.println( "  2 в 5 степене  " + " =" + result);
    }

}


